""" Program to print a substring with first and last characters in the given string """

def print_substring(example_string):
    """
    Function which takes a string as input and prints a substring
    using first and last character in given string
    """

    print("Substring: " + example_string[0] + example_string[-1])

print_substring("Hello World")
print_substring("Hello World12")
print_substring("Hello Honey")
print_substring("Hi World")
print_substring("Krishna is my World")


