""" Program to print a string in the given format """

def make_nametag(name, topic):
    """
    Function that takes two strings as input, prints in the 
    prescribed format.
    """

    print("Hi! My name is {}.This lecture covers {}.".format(name, topic))

make_nametag("Rasheed", "Maths")
make_nametag("Mrinalini", "Graphics")
make_nametag("Sr. Ambika Mary", "English")
