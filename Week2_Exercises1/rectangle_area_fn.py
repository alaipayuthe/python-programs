"""
Write a Python function rectangle_area that takes two parameters width and height corresponding to the lengths of the sides of a rectangle and returns the area of the rectangle in square inches. Area of rectangle template --- Area of rectangle solution
"""

def rectangle_area(width, height):
    return (width * height)

print("Rectangle area(w: 5in, h: 4in): " + str(rectangle_area(5, 4)) + " square inches")
